
class Complex {

    private double real;
    private double imaginary;

    public double getReal() {
        return this.real;
    }

    public double getImaginary() {
        return this.imaginary;
    }

    public void setReal(double newValue) {
        this.real = newValue;
    }

    public void setImaginary(double newValue) {
        this.imaginary = newValue;
    }
}
